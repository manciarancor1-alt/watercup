def bubble_sort(arr):
    """
    Алгоритм пузырьковой сортировки.
    Сортирует массив по возрастанию.
    
    Args:
        arr: список элементов для сортировки
    
    Returns:
        отсортированный список
    """
    n = len(arr)
    # Создаем копию массива, чтобы не изменять оригинал
    arr = arr.copy()
    
    # Проходим по массиву n-1 раз
    for i in range(n - 1):
        # Флаг для оптимизации: если не было обменов, массив уже отсортирован
        swapped = False
        
        # Каждый проход уменьшаем количество сравнений
        for j in range(0, n - i - 1):
            # Если текущий элемент больше следующего, меняем их местами
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True
        
        # Если не было обменов, массив отсортирован - можно завершить
        if not swapped:
            break
    
    return arr


# Пример использования
if __name__ == "__main__":
    # Тестовые данные
    test_array = [64, 34, 25, 12, 22, 11, 90]
    
    print("Исходный массив:", test_array)
    sorted_array = bubble_sort(test_array)
    print("Отсортированный массив:", sorted_array)
    
    # Дополнительные примеры
